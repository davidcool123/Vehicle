public class java {
    //POJO:  Plain Old Java Object
    private String make;
    private String model;

    public java(String make, String model) {
        this.make = make;
        this.model = model;
    }

    public String getMake() {
        return make;
    }

    public String getModel() {
        return model;
    }

    public void setMake(String make) {
        this.make = make;
    }

    public void setModel(String model) {
        this.make = make;

    }

}
